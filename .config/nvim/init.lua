-- Basic settings
vim.opt.showmatch = true         -- show matching brackets
vim.opt.ignorecase = true        -- case insensitive searching
vim.opt.mouse = 'v'              -- enable mouse support in visual mode
vim.opt.hlsearch = true          -- highlight search results
vim.opt.incsearch = true         -- incremental search
vim.opt.tabstop = 4              -- number of columns occupied by a tab
vim.opt.softtabstop = 4          -- see multiple spaces as tabstops so <BS> works properly
vim.opt.expandtab = true         -- convert tabs to spaces
vim.opt.shiftwidth = 4           -- width for automatic indenting
vim.opt.autoindent = true        -- automatically indent new lines
vim.opt.number = true            -- display line numbers
vim.opt.wildmode = 'longest,list' -- bash-like tab completion

vim.wo.number = true
